using Pioran, Test

function test_quad2roots32()
    qa32 = [
        0.025443151049354032,
        0.04252858046335997,
        2.5980088198563633,
    ]
    r32 = [
        -0.012721575524677016 + 0.20583182936448363im,
        -0.012721575524677016 - 0.20583182936448363im,
        -2.5980088198563633 + 0.0im,
    ]
    r = quad2roots(qa32)
    return @test r ≈ r32
end

function test_roots2coeffs32()
    r32 = [
        -0.012721575524677016 + 0.20583182936448363im,
        -0.012721575524677016 - 0.20583182936448363im,
        -2.5980088198563633 + 0.0im,
    ]
    α = roots2coeffs(r32)
    α32 = [
        0.11048962713978024,
        0.10863011129451944,
        2.6234519709057174,
        1,
    ]
    return @test α ≈ α32
end

function test_init_CARMA32()
    rα = [
        -0.042163209825323775 + 1.1115603157767922im,
        -0.042163209825323775 - 1.1115603157767922im,
        -0.7599101571312047 + 0.0im,
    ]
    β = [
        3.9413022090550216,
        11.38193903188344,
        1,
    ]
    𝓒 = CARMA(3, 2, rα, β, 1.3)
