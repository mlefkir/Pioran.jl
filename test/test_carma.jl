end

function test_celerite_coefCARMA()
    rα = [
        -0.042163209825323775 + 1.1115603157767922im,
        -0.042163209825323775 - 1.1115603157767922im,
        -0.7599101571312047 + 0.0im,
    ]
    β = [
        3.9413022090550216,
        11.38193903188344,
        1,
    ]
    𝓒 = CARMA(3, 2, rα, β, 1.3)
    a, b, c, d = Pioran.celerite_coefs(𝓒)
    a_ = [0.666366950927238, 0.666366950927238, -0.03273390185447589]
    b_ = [-0.013410488407876418, 0.013410488407876418, 0.0]
    c_ = [0.042163209825323775, 0.042163209825323775, 0.7599101571312047]
    d_ = [-1.1115603157767922, 1.1115603157767922, -0.0]
    @test a ≈ a_
    @test b ≈ b_
    @test c ≈ c_
    return @test d ≈ d_
end

function test_celerite_repr()
    rα = [
        -0.042163209825323775 + 1.1115603157767922im,
        -0.042163209825323775 - 1.1115603157767922im,
        -0.7599101571312047 + 0.0im,
    ]
    β = [
        3.9413022090550216,
        11.38193903188344,
        1,
    ]
    𝓒 = CARMA(3, 2, rα, β, 1.3)
    C = Pioran.celerite_repr(𝓒)

    @test C isa Pioran.SumOfSemiSeparable

    a, b, c, d = C.a, C.b, C.c, C.d
    a_ = [
        0.666366950927238,
        0.666366950927238,
        -0.03273390185447589,
    ]
    b_ = [
        -0.013410488407876418,
        0.013410488407876418,
        0.0,
    ]
    c_ = [
        0.042163209825323775,
        0.042163209825323775,
        0.7599101571312047,
    ]
    d_ = [
        -1.1115603157767922,
        1.1115603157767922,
        -0.0,
    ]
    @test a ≈ a_
    @test b ≈ b_
    @test c ≈ c_
    return @test d ≈ d_

end

function test_CARMA_ACVF()
    t = LinRange(0, 150, 1000)
    rα = [
        -0.042163209825323775 + 1.1115603157767922im,
        -0.042163209825323775 - 1.1115603157767922im,
        -0.7599101571312047 + 0.0im,
    ]
    β = [
        3.9413022090550216,
        11.38193903188344,
        1,
    ]
    𝓒 = CARMA(3, 2, rα, β, 1.3)
    C = Pioran.celerite_repr(𝓒)
    ACVF_cel = C.(t, 0.0)
    ACVF_carma = Pioran.CARMA_covariance(t, 𝓒)
    return @test ACVF_cel ≈ ACVF_carma
end
